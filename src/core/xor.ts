/**
 * XOR (Exclusive OR)
 * --------------------
 * Self-inverse property: x XOR 0 = x
 * Self-inverse property: x XOR x = 0
 * Commutativity: x XOR y = y XOR x
 * Associative property: `(a XOR b) XOR c` equals to `a XOR (b XOR c)`.
 * Equation like `a = b ^ x` or `a = x ^ b` can be solved like: `x = a ^ b`
 * Distributive property of XOR over addition: `A XOR (B + C)` = (A XOR B) + (A XOR C)`
 */